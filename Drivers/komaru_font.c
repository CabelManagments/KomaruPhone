static int current_font_size = 0;

int komaru_font_width(void) {
    return (current_font_size == 0) ? 5 : (current_font_size == 1) ? 8 : 8;
}

int komaru_font_height(void) {
    return (current_font_size == 0) ? 7 : (current_font_size == 1) ? 8 : 16;
}

void komaru_font_set_size(int size) {
    if(size >= 0 && size <= 2) current_font_size = size;
}

const uint8_t* komaru_font_get_char(char c) {
    if(c < 32 || c > 127) c = 32;
    int idx = c - 32;
    if(current_font_size == 0) return font_5x7[idx];
    if(current_font_size == 1) return font_8x8[idx];
    return font_8x16[idx];
}
