void komaru_icon_draw(int x, int y, const uint8_t *icon, int w, int h) {
    int bytes_per_row = w / 8;
    for(int row = 0; row < h; row++) {
        for(int col = 0; col < w; col++) {
            int byte_idx = row * bytes_per_row + (col / 8);
            int bit = 7 - (col % 8);
            if(icon[byte_idx] & (1 << bit)) {
                komaru_display_pixel(x + col, y + row, 1);
            }
        }
    }
}
